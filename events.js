function toggleArrowBEEDCHED(){
    var arrowToggle = document.querySelector('#toggleBEEDCHED');
    var BEEDCourse = document.getElementById('courseDetailsBEEDCHED');
    var displaySetting = BEEDCourse.style.display;
    arrowToggle.classList.toggle('active');
    document.getElementById('activeBEEDCHED').classList.toggle('bachelorOpen');

    if(displaySetting == 'block'){
        BEEDCourse.style.display = 'none'
    }
    else{
        BEEDCourse.style.display = 'block'
    }
}

